package ArrayList_prgm;

import java.util.ArrayList;

public class ArrayList_GenericType 
{
	public static void main(String[] args) 
	{
		//Generic type of Collection:
		ArrayList<Double> a = new ArrayList();
		a.add(27.5);
		a.add(47.5);
		a.add(99.9);
		a.add(27.6);
		a.add(null);
		a.add(null);
		System.out.println(a);
		
	}
}
