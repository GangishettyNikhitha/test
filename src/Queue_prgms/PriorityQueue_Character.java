package Queue_prgms;

import java.util.PriorityQueue;

public class PriorityQueue_Character 
{
	public static void main(String[] args) 
	{
		PriorityQueue pq = new PriorityQueue<>();
		pq.add('M');
		pq.add('n');
		pq.add('a');
		pq.add('A');
		pq.add('M');
		pq.add('#');
		pq.add('y');
		pq.add('s');
		pq.add('N');
		pq.add('O');
		System.out.println(pq);
		
		
	}
}
